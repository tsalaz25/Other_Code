/* 
 * float_i2f - Return bit-level equivalent of expression (float) x
 *   Result is returned as unsigned int, but
 *   it is to be interpreted as the bit-level representation of a
 *   single-precision floating point values.
 *   Legal ops: Any integer/unsigned operations incl. ||, &&. also if, while
 *   Max ops: 30
 *   Rating: 4
 */
unsigned float_i2f(int x) {
  unsigned sign, ux;
  int p, shift;
  unsigned exp, mant, mask, rest, halfway;

  if (x == 0) return 0;

  sign = (x < 0) ? 0x80000000u : 0u;
  ux   = (x < 0) ? ((unsigned)(~x) + 1u) : (unsigned)x;

  /* find index p of highest set bit (0..31) */
  p = 31;
  while (((ux >> p) & 1u) == 0u) p--;

  exp = (unsigned)(p + 127);

  if (p <= 23) {
    /* shift left; drop hidden 1 */
    mant = (ux << (23 - p)) & 0x7FFFFFu;
    return sign | (exp << 23) | mant;
  } else {
    /* need to right-shift and round-to-even */
    shift = p - 23;
    mant = ux >> shift;                 /* includes leading 1 at bit 23 */

    if (shift > 0) {
      mask    = (1u << shift);    /* low 'shift' bits */
      rest    = ux & mask;
      halfway = 1u << (shift - 1);
    } else {
      rest = 0u; halfway = 0u;
    }

    /* round to nearest, ties to even */
    if (rest > halfway || (rest == halfway && (mant & 1u))) {
      mant += 1u;
      if (mant >> 24) {                 /* carry bumped into hidden bit */
        exp += 1u;
        mant >>= 1;
      }
    }
    mant &= 0x7FFFFFu;                  /* drop hidden 1 */
    return sign | (exp << 23) | mant;
  }
}
/*
 * trueFiveEighths - multiplies by 5/8 rounding toward 0,
 *  avoiding errors due to overflow
 *  Examples: trueFiveEighths(11) = 6
 *            trueFiveEighths(-9) = -5
 *            trueFiveEighths(0x30000000) = 0x1E000000 (no overflow)
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 25
 *  Rating: 4
 */
int trueFiveEighths(int x)
{
    int sign = x >> 31;
  int q = x >> 3;                      /* floor(x/8) via arith shift */
  int r = x & 7;                       /* remainder in [0..7]        */
  int q5 = (q << 2) + q;               /* 5*q                         */
  int rr = (r << 2) + r;               /* 5*r                         */
  int bias = sign & 7;                 /* 7 if negative, else 0       */
  int add = (rr + bias) >> 3;          /* trunc toward 0              */
  return q5 + add;
}
/*
 * trueThreeFourths - multiplies by 3/4 rounding toward 0,
 *   avoiding errors due to overflow
 *   Examples: trueThreeFourths(11) = 8
 *             trueThreeFourths(-9) = -6
 *             trueThreeFourths(1073741824) = 805306368 (no overflow)
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 */
int trueThreeFourths(int x)
{
  int sign = x >> 31;
  int q = x >> 2;                      /* floor(x/4) via arith shift  */
  int r = x & 3;                       /* remainder in [0..3]         */
  int q3 = (q << 1) + q;               /* 3*q                         */
  int rr = (r << 1) + r;               /* 3*r                         */
  int bias = sign & 3;                 /* 3 if negative, else 0       */
  int add = (rr + bias) >> 2;          /* trunc toward 0              */
  return q3 + add;
}

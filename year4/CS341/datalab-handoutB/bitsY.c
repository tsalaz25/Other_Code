/* 
 * greatestBitPos - return a mask that marks the position of the
 *               most significant 1 bit. If x == 0, return 0
 *   Example: greatestBitPos(96) = 0x40
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 70
 *   Rating: 4 
 */
int greatestBitPos(int x) {
  int v = x;
  v |= v >> 1;
  v |= v >> 2;
  v |= v >> 4;
  v |= v >> 8;
  v |= v >> 16;
  return v & ~(v >> 1);
}
/* howManyBits - return the minimum number of bits required to represent x in
 *             two's complement
 *  Examples: howManyBits(12) = 5
 *            howManyBits(298) = 10
 *            howManyBits(-5) = 4
 *            howManyBits(0)  = 1
 *            howManyBits(-1) = 1
 *            howManyBits(0x80000000) = 32
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 90
 *  Rating: 4
 */
int howManyBits(int x) {
  int sign = x >> 31;
  int a = x ^ sign;                 /* if x<0 use ~x, else x */
  int b16 = (!!(a >> 16)) << 4;     a >>= b16;
  int b8  = (!!(a >> 8))  << 3;     a >>= b8;
  int b4  = (!!(a >> 4))  << 2;     a >>= b4;
  int b2  = (!!(a >> 2))  << 1;     a >>= b2;
  int b1  = (!!(a >> 1));           a >>= b1;
  /* a is now 0 or 1 */
  return b16 + b8 + b4 + b2 + b1 + a + 1;
}

/*
 * satAdd - adds two numbers but when positive overflow occurs, returns
 *          maximum possible value, and when negative overflow occurs,
 *          it returns minimum positive value.
 *   Examples: satAdd(0x40000000,0x40000000) = 0x7fffffff
 *             satAdd(0x80000000,0xffffffff) = 0x80000000
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 30
 *   Rating: 4
 */
int satAdd(int x, int y) {
  int sum = x + y;
  int sx = x >> 31, sy = y >> 31, ss = sum >> 31;
  int same = !(sx ^ sy);            /* 1 if x,y same sign */
  int of   = same & (sx ^ ss);      /* 1 if overflow      */

  int tmin = 1 << 31;
  int tmax = ~tmin;

  /* Convert 0/1 flags to full masks */
  int ofmask   = ~of + 1;           /* 0xFFFFFFFF if of, else 0 */
  int pos_over = of & !sx;          /* 1 if +overflow */
  int neg_over = of & !!sx;         /* 1 if -overflow */
  int pmask    = ~pos_over + 1;     /* 0xFFFFFFFF if +overflow */
  int nmask    = ~neg_over + 1;     /* 0xFFFFFFFF if -overflow */

  /* If overflow, pick TMax/TMin; else sum */
  return ( (~ofmask & sum)
         | ( pmask & tmax)
         | ( nmask & tmin) );
}
